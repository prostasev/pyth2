# Вводим строку с арифметическим действием
input_string = input("Введите арифметическое действие (например, 5 + 3): ")

# Убираем лишние пробелы и разделяем строку по оператору '+'
numbers = input_string.replace(" ", "").split("+")

# Преобразуем строки в целые числа
num1 = int(numbers[0])
num2 = int(numbers[1])

# Вычисляем сумму
result = num1 + num2

# Выводим результат
print("Результат:", result)